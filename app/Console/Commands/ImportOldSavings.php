		DB::beginTransaction();
		try {
			$cutoffDate = Carbon::create(2025, 4, 30);

			$count = 0;
			foreach ($lines as $line) {
				if (trim($line) === '') continue;

				$parts = preg_split('/\s+/', trim($line));
				if (count($parts) < 3) continue;

				$empCode = trim($parts[0]);

				// Skip if empCode is #N/A or not numeric
				if (!is_numeric($empCode)) {
					$this->warn("Skipping invalid empCode: {$empCode}");
					continue;
				}

				$amount = floatval(end($parts));

				$employee = Employee::withoutGlobalScope('society_member')
					->where('empCode', $empCode)
					->first();

				if (!$employee) {
					$this->warn("Employee {$empCode} not found. Skipping.");
					continue;
				}

				if ($employee->is_society_member !== 'YES') {
					$employee->update(['is_society_member' => 'YES']);
				}

				// Find or create Savings account
				$account = Account::firstOrCreate([
					'employee_id' => $employee->id,
					'account_type' => 'SAVINGS',
				], [
					'opened_date' => $cutoffDate,
					'status' => 'ACTIVE',
					'current_balance' => 0
				]);

				$account->update([
					'current_balance' => $amount
				]);

				Transaction::create([
					'account_id' => $account->account_id,
					'tx_date' => $cutoffDate,
					'amount' => $amount,
					'tx_type' => 'CREDIT',
					'category' => 'OPENING BALANCE',
					'description' => 'Opening Savings Balance till April 2025',
				]);

				$this->info("Imported Savings for {$empCode} | Amount: {$amount}");
				$count++;
			}
			DB::commit();
			$this->info("Successfully imported {$count} savings accounts.");
		} catch (\Exception $e) {
			DB::rollBack();
			$this->error("Failed to import savings: " . $e->getMessage());
		}
	}
